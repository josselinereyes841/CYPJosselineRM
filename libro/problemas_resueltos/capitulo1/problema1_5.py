RADIO  = float(input(" Escriba el radio del cilindro: "))
ALTU = float(input("Escriba la altura del cilindro: "))
VOLUMEN = 3.141592*(RADIO**2)*ALTU
AREA = 2*3.141592*RADIO*ALTU
print(f"El radio del cilindro es {RADIO}, su altura es {ALTU}, por lo tanto el area es {AREA} y su volumen es {VOLUMEN}") 

